const defaultLocale = "en";
let currentLocale = defaultLocale;

export function t(key) {
  return (dictionaries[currentLocale] && dictionaries[currentLocale][key]) || dictionaries[defaultLocale][key] || key;
}

export function getLocale() {
  return currentLocale;
}

export function localizeText(source) {
  if (source === undefined || source === null) return "";
  if (typeof source === "object" && !Array.isArray(source)) {
    return source[currentLocale] ?? source[defaultLocale] ?? Object.values(source)[0] ?? "";
  }
  return source;
}

export function formatText(key, replacements = {}) {
  const template = t(key);
  return String(template).replace(/\{\{(.*?)\}\}/g, (_, name) => {
    return replacements[name] !== undefined ? String(replacements[name]) : "";
  });
}

export function setLocale(locale) {
  if (!dictionaries[locale]) locale = defaultLocale;
  currentLocale = locale;
  localStorage.setItem("locale", locale);
  translatePage();
  const select = document.getElementById("localeSelect");
  if (select) select.value = currentLocale;
  window.dispatchEvent(new Event("pixel:localeChanged"));
}

export function initLocalization() {
  const saved = localStorage.getItem("locale");
  setLocale(saved || defaultLocale);

  const localeSelect = document.getElementById("localeSelect");
  if (localeSelect) {
    localeSelect.value = currentLocale;
    localeSelect.addEventListener("change", (event) => {
      const value = event.target.value;
      setLocale(value);
    });
  }
}

export function translatePage(root = document.body) {
  if (!root) return;
  document.documentElement.lang = currentLocale;
  const nodes = root.querySelectorAll("[data-i18n]");
  nodes.forEach((el) => {
    const key = el.dataset.i18n;
    if (!key) return;
    const attr = el.dataset.i18nAttr || "textContent";
    const value = t(key);
    if (attr === "textContent") {
      el.textContent = value;
    } else if (attr === "placeholder") {
      el.placeholder = value;
    } else if (attr === "title") {
      el.title = value;
    } else {
      el.setAttribute(attr, value);
    }
  });
}

export function getSupportedLocales() {
  return Object.keys(dictionaries);
}
