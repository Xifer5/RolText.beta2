const defaultLocale = "en";
let currentLocale = defaultLocale;

export function t(key) {
  return (dictionaries[currentLocale] && dictionaries[currentLocale][key]) || dictionaries[defaultLocale][key] || key;
}

export function getLocale() {
  return currentLocale;
}

export function setLocale(locale) {
  if (!dictionaries[locale]) locale = defaultLocale;
  currentLocale = locale;
  localStorage.setItem("locale", locale);
  translatePage();
}

export function initLocalization() {
  const saved = localStorage.getItem("locale");
  setLocale(saved || defaultLocale);
}

export function translatePage(root = document.body) {
  if (!root) return;
  document.documentElement.lang = currentLocale;
  const nodes = root.querySelectorAll("[data-i18n]");
  nodes.forEach((el) => {
    const key = el.dataset.i18n;
    if (!key) return;
    const attr = el.dataset.i18nAttr || "textContent";
    const value = t(key);
    if (attr === "textContent") {
      el.textContent = value;
    } else if (attr === "placeholder") {
      el.placeholder = value;
    } else if (attr === "title") {
      el.title = value;
    } else {
      el.setAttribute(attr, value);
    }
  });
}

export function getSupportedLocales() {
  return Object.keys(dictionaries);
}
